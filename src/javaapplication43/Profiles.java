    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        // TODO add your handling code here:
        Function func = new Function();
        Connection conn = ConnectionBuilder.getConnection();
        try {
            
            String sqlCmd = "select * from customer where username = ?";
            PreparedStatement pstm = conn.prepareStatement(sqlCmd);
            pstm.setString(1, username);
            ResultSet rs = pstm.executeQuery();
      
            if (rs.next()) {
                usern.setText(rs.getString("username"));
                firstname.setText(rs.getString("fName"));
                lastname.setText(rs.getString("lName"));
                gender.setText(rs.getString("gender"));
                birthday.setText(rs.getString("birthdate"));
                phone.setText(rs.getString("phone"));
                email.setText(rs.getString("email"));
            }

            conn.close();
            
        } catch (Exception ex) {
            ex.printStackTrace();
        }
        conn = ConnectionBuilder.getConnection();
        String locate="";
        try {
            String sqlCmd = "select * from location where custId = ? ORDER BY locationID DESC";
            PreparedStatement pstm = conn.prepareStatement(sqlCmd);
            pstm.setInt(1, func.getCustId(username));
            ResultSet rs = pstm.executeQuery();
      
            if (rs.next()) {
                locate = rs.getString("houseNo")+" "+rs.getString("street")+" "+rs.getString("district")+" "+rs.getString("province") +" "+rs.getString("postcode");

                location.setText(locate);
            }

            conn.close();
            
        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }//GEN-LAST:event_formComponentShown

